package day16_to_day20.day16_FoorLoop;

public class C06_nestedForLoop {
    public static void main(String[] args) {

       /*
       *
       * *
       * * *
       * * * *
       * * * * *
        */
        for (int i = 1; i <=5; i++) {
            for (int j = 1; j <=i ; j++) {
                System.out.print("* ");
            }
            System.out.println("");


        }





    }
}
